/*Calculator.Mod*/
export let Calculate = class calculate{
     constructor(numOne, numTwo){
          this.numOne = numOne;
          this.numTwo = numTwo;
     }
     Addition(){
          return this.numOne + this.numTwo;
     }
     Subtraction(){
          return this.numOne - this.numTwo;
     }
     Multiplication(){
          return this.numOne - this.numTwo;
     }
     Division(){
          return this.numOne - this.numTwo;
     }
     Modulo(){
          return this.numOne - this.numTwo;
     }
}
